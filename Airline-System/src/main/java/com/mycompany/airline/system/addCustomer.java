    public void AutoID() {
        try {
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_project","root","ayush123");
            pre = con.prepareStatement("Select MAX(CustomarID) from customer");
            ResultSet res = pre.executeQuery();
            
            res.next();
            if(res .getString("MAX(CustomarID)") == null) {
                cusID.setText("CS001");
            }else {
                long id = Long.parseLong(res.getString("MAX(CustomarID)").substring(2,res.getString("MAX(CustomarID)").length()));
                 id++;
                 cusID.setText("CS"+String.format("%03d", id));
            }
        } catch (ClassNotFoundException ex){
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE,null,ex);
        } catch ( SQLException ex){
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
