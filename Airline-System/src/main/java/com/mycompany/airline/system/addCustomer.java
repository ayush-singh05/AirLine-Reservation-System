    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            // TODO add your handling code here:
            String Customarid = cusID.getText();
            String Firstname = firstName.getText();
            String Lastname = lastName.getText();
            String passportn = passN.getText();
            String nationalid = natN.getText();
            String addr = address.getText();
            String contactn = contact.getText();
            
            String DOB = date.getText();
            String gender = "";
            if(male.isSelected()){
                gender = "male";
            }else {
                gender = "female";
            }
            
            Connection con;
            PreparedStatement pre;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/airline_project","root","ayush123");
            pre = con.prepareStatement("insert into customer(CustomarID,FirstName,LastName,Passport,National_ID,Address,Contact,Gender,DOB)VALUES(?,?,?,?,?,?,?,?,?)");
            pre.setString(1, Customarid);
            pre.setString(2,Firstname);
            pre.setString(3,Lastname);
            pre.setString(4,passportn);
            pre.setString(5,nationalid);
            pre.setString(6,addr);
            pre.setString(7,contactn);
            pre.setString(8,gender);
            pre.setString(9,DOB);
            
            if(!Firstname.isEmpty() && !Lastname.isEmpty() && !passportn.isEmpty() && !nationalid.isEmpty() && !addr.isEmpty() && !contactn.isEmpty() && !gender.isEmpty() && !DOB.isEmpty()) {
                pre.executeUpdate();
                JOptionPane.showMessageDialog(null, "Customer added Successfully");
            }else if(contactn.length() != 10 &&  contactn.isEmpty() == false) {
            
            JOptionPane.showMessageDialog(null, "Incorrect Contact Details");
            
        }else{
                JOptionPane.showMessageDialog(null, "Fill all Details");
            }
           
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(addCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

